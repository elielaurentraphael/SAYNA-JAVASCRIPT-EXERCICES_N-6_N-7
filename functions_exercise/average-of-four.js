// Ã‰crivez une fonction `moyenneDeQuatre(num1, num2, num3, num4)` qui accepte quatre nombres
// comme arguments.
// La fonction doit renvoyer la moyenne des quatre nombres.

function moyenneDeQuatre(num1, num2, num3, num4) {
    return (num1 + num2 + num3 + num4) / 4;
};

console.log(moyenneDeQuatre(10, 4, 12, 3)); // 7,25
console.log(moyenneDeQuatre(-20, 50, 4, 21)); // 13,75
console.log(moyenneDeQuatre(10, 4, 12, 3)); // 7.25
console.log(moyenneDeQuatre(5, 5, 3, 7)); // 5